import { LitElement, html } from 'https://esm.archive.org/lit-element'
import Fireworks from 'https://esm.archive.org/fireworks-canvas'

// eslint-disable-next-line no-console
const log = console.log.bind(console) // Stateless function, global to all methods

const NCOLS = 5
const NROWS = 6

class WordsmithGame extends LitElement {
  static get properties() {
    return {
      letters: Object,
      picked: Object,
      nonword: Boolean,
      won: Boolean,
    }
  }

  constructor() {
    super()

    // eslint-disable-next-line no-use-before-define
    const words = Words.words()
    const word = words[Math.floor(words.length * Math.random())]
    log(word)
    this.answer = word
    this.picked = []
  }

  firstUpdated() {
    super.firstUpdated()
    document.addEventListener('keyup', (evt) => this.keyup(evt))
  }

  chars_changed(nonword) {
    const ltrs = document.getElementsByTagName('ws-ltr')
    for (let i = 0; i < ltrs.length; i++) {
      ltrs[i].picked = JSON.parse(JSON.stringify(this.picked))
      ltrs[i].nonword = nonword
      if (i < NCOLS * NROWS)
        ltrs[i].val = this.picked[i]
    }
  }

  keyup(evt) {
    const key = evt.key.toLowerCase()

    if (key === 'backspace' || key === 'delete')
      this.picked.pop()
    else if (!this.nonword && key.length === 1 && key >= 'a' && key <= 'z')
      this.picked.push(key)
    else
      return log(key)

    // when we "win", update the character states one last time
    let { won } = this
    this.nonword = false
    if (!(this.picked.length % NCOLS)) {
      // a row is "finished" -- but if the current row isnt a word in our list, reject row "finish"
      const word = this.picked.slice(-5).join('')
      if (word === this.answer) {
        document.getElementById('spacebar').getElementsByClassName('ltr')[0].innerHTML =
          '<div id="space-msg" class="fade-in">spacebar says:<br> great job, novelist!</div>'

        setTimeout(() => document.getElementsByTagName('body')[0].classList.add('flip'), 1500)
        setTimeout(() => document.getElementsByTagName('body')[0].classList.remove('flip'), 2500)
        setTimeout(WordsmithGame.fireworks, 3500)
        won = true
      }
      // eslint-disable-next-line no-use-before-define
      if (this.picked.length && !(Words.words().includes(word))) {
        this.nonword = true
        document.getElementById('spacebar').getElementsByClassName('ltr')[0].innerHTML =
          '<div id="space-msg" class="fade-in">spacebar says:<br> not a word in my list</div>'
        setTimeout(() => {
          document.getElementById('space-msg').classList.remove('fade-in')
        }, 1200)
      }
    }

    if (!this.won)
      this.chars_changed(this.nonword)

    this.won = won // if we _just_ won, this ensures we wont call chars_changed again
    return true
  }

  render() {
    let n = 0
    return html`
      <div id="ltrs">
        ${[...Array(NROWS).keys()].map(() => html`
          <div>${[...Array(NCOLS).keys()].map(
    // eslint-disable-next-line indent, function-paren-newline
            () => html`<ws-ltr answer=${this.answer} n=${n++}></ws-ltr>`)}
          </div>`)}
      </div>

      <div id="scoring">
        <div>
          ${'qwertyuiop'.split('').map(
    // eslint-disable-next-line indent, function-paren-newline
           (v) => html`<ws-ltr answer=${this.answer} n=${n++} val="${v}"></ws-ltr>`)}
          <ws-ltr class="backspace" val="back space"></ws-ltr>
        </div>
        <div>
          ${'asdfghjkl'.split('').map(
    // eslint-disable-next-line indent, function-paren-newline
            (v) => html`<ws-ltr answer=${this.answer} n=${n++} val="${v}"></ws-ltr>`)}
        </div>
        <div>
          ${'zxcvbnm'.split('').map(
    // eslint-disable-next-line indent, function-paren-newline
            (v) => html`<ws-ltr answer=${this.answer} n=${n++} val="${v}"></ws-ltr>`)}
          <ws-ltr val="?"></ws-ltr>
        </div>
          <ws-ltr id="spacebar"></ws-ltr>
        <div>
        </div>
      </div>`
  }

  static fireworks() {
    const width = window.innerWidth
    const fx = new Fireworks(document.getElementById('ltrs'), {
      maxRockets: 7,
      rocketSpawnInterval: 150,
      numParticles: 100,
      explosionMinHeight: 0.5,
      explosionMaxHeight: 0.9,
      explosionChance: 0.08,
      width,
      height: 500,
      cannons: [{ x: width * 0.4 }, { x: width * 0.6 }],
      rocketInitialPoint: width * 0.5,
    })
    const stop = fx.start()
    setTimeout(() => stop(), 5000)
  }

  createRenderRoot() { return this } // omit shadow DOM CSS
}

customElements.define('ws-ltr', class extends LitElement {
  static get properties() {
    return {
      val: String,
      n: Number,
      answer: String,
      picked: Object,
      nonword: Boolean,
      state: String,
    }
  }

  constructor() {
    super()
    this.picked = []
    this.answer = ''
  }

  render() {
    if (typeof this.scoring === 'undefined')
      this.scoring = typeof this.val !== 'undefined'

    if (!this.nonword && !this.state) {
      if (this.picked.length && !(this.picked.length % NCOLS)) {
        const answer = this.answer.split('')

        this.state = ''
        if (this.scoring) {
          if (this.picked.includes(this.val)) {
            if (answer.includes(this.val)) {
              this.state = 'warning'
              for (let n = 0; n < this.picked.length; n++) {
                if (answer[n % NCOLS] === this.picked[n] && answer[n % NCOLS] === this.val)
                  this.state = 'success'
              }
            } else {
              this.state = 'danger'
            }
          }
        } else if (this.val) {
          if (this.picked[this.n] === answer[this.n % NCOLS])
            this.state = 'success'
          else
            this.state = answer.includes(this.val) ? 'warning' : 'danger'
        }
      }
    }

    return html`
      <div class="ltr-wrap">
        <div class="ltr alert alert-${this.state} text-${this.state}" role="alert">
          ${this.val}
        </div>
      </div>`
  }

  createRenderRoot() { return this } // omit shadow DOM CSS
})

